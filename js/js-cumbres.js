lightbox = function() {

  /*atributos generales, incluyendo fondo*/
  $("#popup_bg").css({
    width: $(window).width() + "px",
    height: $(window).height() + "px",
    opacity: "0.5",
    filter: "alpha(opacity=40)",
    position: "fixed",
    background: "#000",
    left: "0px",
    top: "0px",
    zIndex: "100000"
  });
  /*efecto al cerrar*/
  $("#popup div.cerrar,#popup_bg").click(function() {
    $("#popup, #popup_bg").fadeOut(1000, function() {
      $("#popup, #popup_bg").remove();
    })
  })
  /*ventana del pop up*/
  $("#popup").css({
    width: "550px",
    height: "300px",
    position: "absolute",
    left: "28%",
    top: "30%",
    zIndex: "100001",
    background: "#2b353b",
    paddingRight: "20px",
    borderRadius: "15px"
  });
  /*boton cerrar*/
  $("#popup div.cerrar").css({
    width: "27px",
    height: "25px",
    position: "absolute",
    marginLeft: "538px",
    marginTop: "14px"
  });

  /*texto o imagen*/
  $("#popup div#popup_contenido").css({
    height: "330px",
    marginLeft: "15px",
    marginTop: "8%",
    textAlign: "center",
    wordWrap: "break-word",
    fontFamily: "Open Sans",
    color: "#fff",
  });
}

